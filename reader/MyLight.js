/**
* MyLight Constructor
* saves the information about the lights read from the XML
* @constructor
*/
function MyLight() {
     this.id;
     this.enable;
     this.positionX;
     this.positionY;
     this.positionZ;
     this.ambient;
     this.diffuse;
     this.specular;
 };

MyLight.prototype.constructor = MyLight;